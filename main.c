int main(void) {

	char str[40];
	char temp[40];
    LCD_Config();
    FMC_NAND_Init();
    ShowSplashScreen();

    // Initialize touch controller
    if (TM_STMPE811_Init() != TM_STMPE811_State_Ok) {
        // Display error message
        TM_ILI9341_Puts(10, 10, "Touch Error!", &TM_Font_11x18, ILI9341_COLOR_RED, ILI9341_COLOR_BLACK);
        while(1);
    }

    // Small Delay
    for(int i = 0; i < 1000; i++);

    // Set touch orientation to match LCD
    TouchData.orientation = TM_STMPE811_Orientation_Landscape_2;

    cur_mode = MENU_MODE;
    TM_ILI9341_Fill(ILI9341_COLOR_BLACK);
    DrawButtons();

    // Main Loop
    while (1) {

    	/* Handle user input */
		UI_Handler();

		switch (cur_mode){

			case MENU_MODE:

				break;

			case DMM_MODE:

				if (cap_measurement_active) {

					ProcessCapacitanceMeasurement();

					// Update display when measurement is done
					if (cap_measurement_done) {
						cap_measurement_done = 0;

						// Clear only the display area
						 TM_ILI9341_DrawFilledRectangle(10, 50, LCD_WIDTH - 10, LCD_HEIGHT/2 - 10, ILI9341_COLOR_BLACK);

						float_to_str(capacitance_value, temp, 3);

						if (capacitance_value < 1.0f) {
							snprintf(str, sizeof(str), "%s uF", temp);
						}
						else if (capacitance_value < 1000.0f) {
							snprintf(str, sizeof(str), "%s uF", temp);
						}
						else {
							float mF_value = capacitance_value / 1000.0f;
							float_to_str(mF_value, temp, 3);
							snprintf(str, sizeof(str), "%s mF", temp);
						}
						DisplayCenteredTextInRectangle(DMM_DISPLAY_X, DMM_DISPLAY_Y, DMM_DISPLAY_WIDTH, DMM_DISPLAY_HEIGHT, str, &TM_Font_11x18, ILI9341_COLOR_WHITE);
					}
				}

				break;

			case OSCI_MODE:

				if (refresh) {
					refresh = 0;

					// Only process new data if not in recall mode
					if (!recall_mode) {

						/* Process ADC Data */
						DrawGrid();
						CalculateMeasurement((uint32_t*)adc_buffer);

						/* Update Display */
						PlotWaveform();
						DisplayMeasurement();

					}
					else {

						// Display recalled waveform
						DrawGrid();

						if (recall_plot) {
							recall_plot = 0;

							PlotWaveform();
							CalculateMeasurement((uint32_t*)recalled_waveform);
							DisplayMeasurement();
						}


					}
				}

				break;
		}
		// Small Delay
		for(int i = 0; i < 1000; i++);
    }
}

