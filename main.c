void UI_Handler(void) {
                     static uint32_t last_touch_time = 0;
    uint32_t current_time = SysTick->VAL;

    // Add debouncing - only check touch every 200ms
    if (current_time - last_touch_time < 200) {
        return;
    }

    // Read touch data
    if (TM_STMPE811_ReadTouch(&TouchData) == TM_STMPE811_State_Pressed) {
        last_touch_time = current_time;

        switch (cur_mode){

			case MENU_MODE:
				ProcessMainMenuTouch(TouchData.x, TouchData.y);
				break;

			case DMM_MODE:

				ProcessDmmMenuTouch(TouchData.x, TouchData.y);
				break;

			case OSCI_MODE:
				ProcessOSCITouch(TouchData.x, TouchData.y);
				break;
        }


    }
}

void ProcessMainMenuTouch(uint16_t x, uint16_t y){

	int8_t button_id = TM_ILI9341_Button_Touch(&TouchData);

	if (button_id >= 0) {

		if (button_id == dmm_button_id) {
			cur_mode = DMM_MODE;
			DMM_BUTTON_SEL = 0;
			// Initialize DMM
			TM_ILI9341_Fill(ILI9341_COLOR_BLACK);
			DrawButtons();
		}

		else if (button_id == osci_button_id) {

			cur_mode = OSCI_MODE;
			// Initialize oscilloscope
			OSCI_Config();
			OSCI_Timer(time_bases[cur_time_base]);
			OSCIDMA_Config();
			Waveform_Timer();


			CH1_freq = CH1_avg = CH1_duty = CH1_vpp = 0;
			CH2_freq = CH2_avg = CH2_duty = CH2_vpp = 0;

			// Draw oscilloscope UI
			TM_ILI9341_Fill(ILI9341_COLOR_BLACK);
			DrawGrid();
			DrawButtons();
		}
	}

}

