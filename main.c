// Microsecond delay using TIM2
void CAP_delay_us(uint32_t us) {
    uint32_t start = TIM2->CNT;
    while ((TIM2->CNT - start) < us) {}
}

void ProcessCapacitanceMeasurement(void) {
    static uint32_t last_measurement_time = 0;
    uint32_t current_time = TIM2->CNT;

    // Add debouncing - measure only every 100ms when active
    if (current_time - last_measurement_time < 100000) { // 100ms in microseconds
        return;
    }
    last_measurement_time = current_time;

    switch (capState) {
        case CAP_STATE_DISCHARGED:
            // Force discharge
            Cap_Discharge();
            CAP_delay_us(DISCHARGE_SETTLE_US);

            capState = CAP_STATE_CHARGING;
            cap_start_time = TIM2->CNT;
            break;

        case CAP_STATE_CHARGING:
            // Start charging
            Cap_Charge();

            // Read voltage and check threshold
            uint16_t adc_val = CAP_ADC1_Read();
            uint32_t elapsed_us = TIM2->CNT - cap_start_time;

            if (adc_val >= ADC_THRESHOLD_COUNT) {
                // Voltage reached threshold - calculate capacitance
                float alpha = (float)ADC_THRESHOLD_COUNT / ADC_MAX;
                float k = -logf(1.0f - alpha); // ln(Vs/(Vs-Vth))
                float t_sec = (float)elapsed_us * 1e-6f;

                capacitance_value = t_sec / (RESISTOR * k); // in Farads
                capacitance_value *= 1e6f; // Convert to microfarads

                capState = CAP_STATE_MEASURED;
                cap_measurement_done = 1;

                // Switch to discharge mode
                Cap_Discharge();
            }

            // Timeout protection (5 seconds max)
            if (elapsed_us > TIMEOUT_US) {
                capacitance_value = 0.0f; // Indicate timeout/error
                capState = CAP_STATE_DISCHARGED;
                cap_measurement_done = 1;
                cap_measurement_active = 0;
                Cap_Discharge();
            }
            break;

        case CAP_STATE_MEASURED:
            // Measurement complete, wait for user to exit or restart
            // Small delay before next measurement cycle
            CAP_delay_us(200000); // 200ms delay
            capState = CAP_STATE_DISCHARGED;
            break;
    }
}
