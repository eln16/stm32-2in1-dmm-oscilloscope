void ProcessOSCITouch(uint16_t x, uint16_t y) {
    // Check if any button was pressed
    int8_t button_id = TM_ILI9341_Button_Touch(&TouchData);

    if (button_id >= 0) {

    	// Check if it's menu button
		if (button_id == main_menu_button_id) {
			OSCI_Deinit();
			CH1_flag = 0;
			CH2_flag = 0;
			recall_mode = 0;
			cur_mode = MENU_MODE;
			TM_ILI9341_Fill(ILI9341_COLOR_BLACK);
			DrawButtons();
			return;
		}

        // Check if it's the timebase button
        if (button_id == timebase_button_id) {
            // Cycle through time bases
            cur_time_base = (cur_time_base + 1) % 7;
            OSCI_Timer(time_bases[cur_time_base]);

            // Erase Graph
            TM_ILI9341_DrawFilledRectangle(GRID_X + 1, GRID_Y + 1, GRID_WIDTH - 2, GRID_HEIGHT - 2, ILI9341_COLOR_BLACK);
            DrawGrid();

            // Update display
            PlotWaveform();
            DisplayMeasurement();
            DrawButtons();
        }
        // Check if it's channel 1 button
        else if (button_id == ch1_button_id) {
            CH1_flag = !CH1_flag;

            // Erase Graph
            for (int x = 1; x < MAX_POINTS; x++) {
				if (!CH1_flag) {
					TM_ILI9341_DrawLine(GRID_X + x - 1, old_y_ch1[x - 1], GRID_X + x, old_y_ch1[x], ILI9341_COLOR_BLACK);
				}
				if (!CH2_flag) {
					TM_ILI9341_DrawLine(GRID_X + x - 1, old_y_ch2[x - 1], GRID_X + x, old_y_ch2[x], ILI9341_COLOR_BLACK);
				}
			}
			DrawGrid();

            // Update display
            PlotWaveform();
            DisplayMeasurement();
            DrawButtons();
        }
        // Check if it's channel 2 button
        else if (button_id == ch2_button_id) {
            CH2_flag = !CH2_flag;

            // Erase Graph
            for (int x = 1; x < MAX_POINTS; x++) {
				if (!CH1_flag) {
					TM_ILI9341_DrawLine(GRID_X + x - 1, old_y_ch1[x - 1], GRID_X + x, old_y_ch1[x], ILI9341_COLOR_BLACK);
				}
				if (!CH2_flag) {
					TM_ILI9341_DrawLine(GRID_X + x - 1, old_y_ch2[x - 1], GRID_X + x, old_y_ch2[x], ILI9341_COLOR_BLACK);
				}
			}
			DrawGrid();

            // Update display
            PlotWaveform();
            DisplayMeasurement();
            DrawButtons();
        }

        // Check if it's save button
        else if (button_id == save_button_id) {
            // Save current waveform to flash
        	SaveWaveform_NAND();

        	recall_CH1 = CH1_flag;
        	recall_CH2 = CH2_flag;
        }

        // Check if it's recall button
        else if (button_id == recall_button_id) {

        	recall_plot = 1;
        	recall_mode = !recall_mode;

        	if (recall_mode) {
				// First press - load and display saved waveform

				RecallWaveform_NAND();


			}
        	else {
				// Second press - clear recalled waveform and return to live mode
				// Erase Graph
        		for (int x = 1; x < MAX_POINTS; x++) {
					if (!CH1_flag) {
						TM_ILI9341_DrawLine(GRID_X + x - 1, recall_old_y_ch1[x - 1], GRID_X + x, recall_old_y_ch1[x], ILI9341_COLOR_BLACK);
					}
					if (!CH2_flag) {
						TM_ILI9341_DrawLine(GRID_X + x - 1, recall_old_y_ch2[x - 1], GRID_X + x, recall_old_y_ch2[x], ILI9341_COLOR_BLACK);
					}
				}

			}

			DrawButtons();
        }
    }
}

