void CalculateMeasurement(uint32_t* interleaved_samples) {

    uint16_t min_ch1 = 4095, max_ch1 = 0;
    uint16_t min_ch2 = 4095, max_ch2 = 0;
    uint32_t sum_ch1 = 0, sum_ch2 = 0;

    // Process all samples for both channels separately
    for (int i = 0; i < BUFFER_SIZE; i++) {
        // Extract both channels
    	uint16_t sample_ch1 = interleaved_samples[i] & 0xFFFF;         // Lower 16 bits (CH1)
    	uint16_t sample_ch2 = (interleaved_samples[i] >> 16) & 0xFFFF; // Upper 16 bits (CH2)

        // CH1 calculations
        if (sample_ch1 < min_ch1) min_ch1 = sample_ch1;
        if (sample_ch1 > max_ch1) max_ch1 = sample_ch1;
        sum_ch1 += sample_ch1;

        // CH2 calculations
        if (sample_ch2 < min_ch2) min_ch2 = sample_ch2;
        if (sample_ch2 > max_ch2) max_ch2 = sample_ch2;
        sum_ch2 += sample_ch2;
    }

    // Calculate measurements for CH1
    CH1_vpp = ((max_ch1 - min_ch1) * 3.3f) / 4095.0f;
    CH1_avg = (sum_ch1 / (float)BUFFER_SIZE) * 3.3f / 4095.0f;

    // Calculate measurements for CH2
    CH2_vpp = ((max_ch2 - min_ch2) * 3.3f) / 4095.0f;
    CH2_avg = (sum_ch2 / (float)BUFFER_SIZE) * 3.3f / 4095.0f;

    // Calculate frequency for both channels

	float period =  SAMPLES_PER_DIV / time_bases[cur_time_base];
	CH1_freq = period;

	period =  SAMPLES_PER_DIV / time_bases[cur_time_base];
	CH2_freq = period;


    // Duty cycle measurement for both channels
    if (max_ch1 - min_ch1 > 100) {
        int high_count_ch1 = 0;
        uint16_t threshold_ch1 = (max_ch1 + min_ch1) / 2;

        for (int i = 0; i < BUFFER_SIZE; i++) {
            uint16_t sample_ch1 = interleaved_samples[i];
            if (sample_ch1 > threshold_ch1) {
                high_count_ch1++;
            }
        }
        CH1_duty = (high_count_ch1 / (float)BUFFER_SIZE) * 100.0f;
    }
    else {
        CH1_duty = 0;
    }

    if (max_ch2 - min_ch2 > 100) {
        int high_count_ch2 = 0;
        uint16_t threshold_ch2 = (max_ch2 + min_ch2) / 2;

        for (int i = 0; i < BUFFER_SIZE; i++) {
            uint16_t sample_ch2 = interleaved_samples[i];
            if (sample_ch2 > threshold_ch2) {
                high_count_ch2++;
            }
        }
        CH2_duty = (high_count_ch2 / (float)BUFFER_SIZE) * 100.0f;
    }
    else {
        CH2_duty = 0;
    }


    if (!CH1_flag){
    	CH1_vpp = 0;
		CH1_avg = 0;
		CH1_freq = 0;
		CH1_duty = 0;
    }

    if (!CH2_flag){
    	CH2_vpp = 0;
		CH2_avg = 0;
		CH2_freq = 0;
		CH2_duty = 0;
    }

}

