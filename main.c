/* Save Waveform Functions for NAND Flash - CORRECTED */
void SaveWaveform_NAND(void) {
	// 1. Define the physical address to write to.
	    //    We'll use Zone 0, Block 10, Page 0.
	    NAND_ADDRESS save_address;
	    save_address.Zone = 0;
	    save_address.Block = 10;
	    save_address.Page = 0;
	    uint8_t *buffer_ptr= (uint8_t *)adc_buffer;

	    int remain_byte = BUFFER_SIZE * 4;
	    static uint8_t page_buffer[NAND_PAGE_SIZE];
		FMC_NAND_EraseBlock(save_address);

	    while(remain_byte > 0){

	    	uint32_t size_cpy = (remain_byte > NAND_PAGE_SIZE) ? NAND_PAGE_SIZE:  remain_byte;

			memcpy(page_buffer, buffer_ptr, size_cpy);

			FMC_NAND_WriteSmallPage(page_buffer, save_address, 1);
			//inc page
			FMC_NAND_AddressIncrement(&save_address);

			remain_byte -= NAND_PAGE_SIZE;
			buffer_ptr += NAND_PAGE_SIZE;
	    }
}

void RecallWaveform_NAND(void) {
	// 1. Define the same address we saved to.
	    NAND_ADDRESS recall_address;
	    recall_address.Zone = 0;
	    recall_address.Block = 10;
	    recall_address.Page = 0;
	    uint8_t *buffer_ptr= (uint8_t *)recalled_waveform;

	    // 2. Read one full page (2048 bytes) from the NAND flash.
	    //    Our 960 bytes of data will be at the beginning of the received data.

	    // Now, 'read_nand_buffer' contains the recalled waveform.
	    // You can proceed to display it on the LCD.

	    int remain_byte = BUFFER_SIZE*4;
		static uint8_t page_buffer[NAND_PAGE_SIZE];
		while(remain_byte>0){
			uint32_t size_read = remain_byte > NAND_PAGE_SIZE ? NAND_PAGE_SIZE:  remain_byte;

			FMC_NAND_ReadSmallPage(page_buffer, recall_address, 1); // Reading 1 page
			//inc page
			FMC_NAND_AddressIncrement(&recall_address);
			memcpy(buffer_ptr, page_buffer, size_read * sizeof(uint32_t));
			remain_byte -=NAND_PAGE_SIZE;
			buffer_ptr+=NAND_PAGE_SIZE;
		}
}


